package java.tutorials;


public class ExecutionExample2 {
    public static void main(String[] args){
        // to declare a variable you need to first write the type of the variable
        // such as "int". "int" means the type is a counting number
        // then you write the variable name such as "a"
        // then you write = to say that you are assigning a value to a variable
        // then you write the value of that variable, such as 3.
        // then you write ";" to end the statement

        // int a = 3;
        int a = 3; // assign the value 3 to a
        int b = 2; // assign the value 2 to b
        int c = a + b; // assign the value of whatever a + b equals, to c
        System.out.println(a); // print the value of a to the console
        System.out.println(b); // print the value of b to the console
        System.out.println(c); // print the value of c to the console

        // when I press the button to execute the code, it does everything really fast one step at a time
        // the computer doesn't complete everything instantly, it's just super fast.
        // we have what is called the point of execution
        // the definition of execution is "carry out or put into effect"

        // currently, the point of execution is right here --> <--
        // currently meaning when I press the button
        int d = 5;
        // now the execution is here --> <--
        System.out.println(d);
        // now the execution is here --> <-- and is about to finish everything
    }
}
